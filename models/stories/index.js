///////////////////////////////////
// IMPORT SCHEMA AND MODEL
///////////////////////////////////
const { Schema, model } = require("mongoose");

////////////////////////////////////
// CREATE SCHEMA
////////////////////////////////////

const storySchema = new Schema({
  username: { type: String, required: true },
  story: String,
}, {timestamps: true});

////////////////////////////////////
// CREATE MODEL
////////////////////////////////////
const Story = model("story", storySchema);

////////////////////////////////////
// EXPORT MODEL
////////////////////////////////////
module.exports = Story;
